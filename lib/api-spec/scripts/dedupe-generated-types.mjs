import { readdirSync, readFileSync, writeFileSync } from "node:fs";
import { join, dirname, resolve } from "node:path";
import { fileURLToPath } from "node:url";

const here = dirname(fileURLToPath(import.meta.url));
const apiZodSrc = resolve(here, "..", "..", "api-zod", "src");
const typesDir = join(apiZodSrc, "generated", "types");
const apiPath = join(apiZodSrc, "generated", "api.ts");
const indexPath = join(apiZodSrc, "index.ts");

const declRe = /^export\s+(?:interface|type)\s+([A-Za-z0-9_]+)\b/gm;

const typeNames = new Set();
let touched = 0;

for (const file of readdirSync(typesDir)) {
  if (!file.endsWith(".ts")) continue;
  const path = join(typesDir, file);
  const src = readFileSync(path, "utf8");

  const seen = new Map();
  let m;
  declRe.lastIndex = 0;
  while ((m = declRe.exec(src)) !== null) {
    const name = m[1];
    if (!seen.has(name)) seen.set(name, []);
    seen.get(name).push(m.index);
  }

  const dupNames = [...seen.entries()]
    .filter(([, idxs]) => idxs.length > 1)
    .map(([n]) => n);

  let out = src;
  for (const name of dupNames) {
    const blockRe = new RegExp(
      `export\\s+interface\\s+${name}\\s*\\{[\\s\\S]*?\\n\\}\\s*\\n?`,
      "g",
    );
    const before = out;
    out = out.replace(blockRe, "");
    if (out === before) {
      const typeRe = new RegExp(
        `export\\s+type\\s+${name}\\s*=[\\s\\S]*?;\\s*\\n?`,
        "g",
      );
      let first = true;
      out = out.replace(typeRe, (match) => {
        if (first) {
          first = false;
          return match;
        }
        return "";
      });
    }
  }

  if (out !== src) {
    writeFileSync(path, out);
    touched++;
    console.log(
      `[dedupe-generated-types] cleaned ${file} (${dupNames.join(", ")})`,
    );
  }

  for (const name of seen.keys()) typeNames.add(name);
}

const apiSrc = readFileSync(apiPath, "utf8");
const apiExportRe = /^export\s+const\s+([A-Za-z0-9_]+)\b/gm;
const apiNames = new Set();
let am;
while ((am = apiExportRe.exec(apiSrc)) !== null) apiNames.add(am[1]);

const sortedTypes = [...typeNames].sort();
const indexLines = [
  "// AUTO-GENERATED by lib/api-spec/scripts/dedupe-generated-types.mjs.",
  "// Do not edit manually. Re-run `pnpm --filter @workspace/api-spec codegen`.",
  "",
  'export * from "./generated/api";',
  "",
  "// Explicit type re-exports override the wildcard above so the TypeScript",
  "// type for each schema name resolves to the inferred shape from",
  "// `generated/types/`, not the ZodObject value-type from `generated/api`.",
  "export type {",
  ...sortedTypes.map((n) => `  ${n},`),
  '} from "./generated/types";',
  "",
];
writeFileSync(indexPath, indexLines.join("\n"));
console.log(
  `[dedupe-generated-types] wrote ${indexPath} with ${sortedTypes.length} type re-exports`,
);

if (touched === 0) {
  console.log("[dedupe-generated-types] no duplicate declarations to clean");
}
